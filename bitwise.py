# Define two integers
a = 5   # 101 in binary
b = 3   # 011 in binary

# Bitwise AND (&)
print("Bitwise AND (&):")
print(f"{a} & {b} = {a & b}")  # Output: 1

# Bitwise OR (|)
print("\nBitwise OR (|):")
print(f"{a} | {b} = {a | b}")  # Output: 7

# Bitwise XOR (^)
print("\nBitwise XOR (^):")
print(f"{a} ^ {b} = {a ^ b}")  # Output: 6

# Bitwise NOT (~)
print("\nBitwise NOT (~):")
print(f"~{a} = {~a}")  # Output: -6

# Left Shift (<<)
print("\nLeft Shift (<<):")
print(f"{a} << 1 = {a << 1}")  # Output: 10

# Right Shift (>>)
print("\nRight Shift (>>):")
print(f"{a} >> 1 = {a >> 1}")  # Output: 2
